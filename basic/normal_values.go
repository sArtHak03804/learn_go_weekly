// normal_values demonstrates printing different types of literals in Go.
package main

import "fmt"


func normal_values() {
    fmt.Println("we can see this program as diffrent types of printing ways")
    fmt.Println("Integer:", 42)
    fmt.Println("Float:", 3.14)
    fmt.Println("String:", "Go Programming")
    fmt.Println("Boolean:", true)
}
